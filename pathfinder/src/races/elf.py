"""
@file elf.py
@brief Класс расы Эльф.

@details
Реализация расы Эльф с особенностями передвижения по различным типам местности.
Эльфы получают значительный бонус в лесах и на равнинах, но замедляются в
пещерах, деревнях и замках.
"""

from .race import Race
from src.hex.hex_terrain_type import HexTerrainType


class Elf(Race):
    """
    @brief Класс представляющий расу Эльф.
    
    @details
    Эльфы - древняя раса, тесно связанная с природой, особенно с лесами.
    Они получают значительный бонус при передвижении по лесам и природным 
    территориям, но менее комфортно чувствуют себя в закрытых пространствах
    и населенных пунктах других рас.
    """
    
    # Имя расы
    name = "Эльф"
    
    # Описание расовых особенностей
    description = """
    Эльфы - древняя раса, живущая в гармонии с природой. Они получают значительный 
    бонус в лесах (-50% к стоимости передвижения) и на равнинах, но медленнее 
    передвигаются в пещерах, деревнях и замках других рас.
    """
    
    # Модификаторы стоимости прохода для разных типов местности
    terrain_modifiers = {
        HexTerrainType.ROAD: 0.8,     # По дорогам эльфы двигаются быстрее обычного
        HexTerrainType.GRASS: 0.7,    # По равнинам очень быстро
        HexTerrainType.FOREST: 0.5,   # В лесах получают большой бонус
        HexTerrainType.HILLS: 0.9,    # На холмах небольшой бонус
        HexTerrainType.MOUNTAIN: 1.5,  # По горам медленнее, но могут проходить
        HexTerrainType.WATER: float('inf'),  # Вода непроходима
        HexTerrainType.SWAMP: 1.0,    # В болотах - обычная скорость
        HexTerrainType.DESERT: 1.3,   # В пустыне замедляются
        HexTerrainType.SNOW: 1.1,     # В снегу небольшое замедление
        HexTerrainType.LAVA: float('inf'),   # Лава непроходима
        HexTerrainType.CASTLE: 1.2,   # В замках замедляются
        HexTerrainType.VILLAGE: 1.1,  # В деревнях замедляются
        HexTerrainType.CAVE: 1.5,     # В пещерах значительно замедляются
        HexTerrainType.WALL: float('inf'),  # Стены непроходимы
        HexTerrainType.START: 1.0,    # Стартовая позиция - обычная скорость
        HexTerrainType.END: 1.0       # Конечная позиция - обычная скорость
    } 