"""
@file human.py
@brief Класс расы Человек.

@details
Реализация расы Человек с особенностями передвижения по различным типам местности.
Люди получают значительный бонус на дорогах и в деревнях, но замедляются в лесах,
пустынях и снежных регионах.
"""

from .race import Race
from src.hex.hex_terrain_type import HexTerrainType


class Human(Race):
    """
    @brief Класс представляющий расу Человек.
    
    @details
    Люди - приспособляемая раса, которая построила развитую цивилизацию
    с дорогами, деревнями и замками. Они получают значительный бонус при
    движении по дорогам и в населенных пунктах, но менее приспособлены
    к природным условиям, особенно к лесам и экстремальным средам.
    """
    
    # Имя расы
    name = "Человек"
    
    # Описание расовых особенностей
    description = """
    Люди - самая многочисленная и приспособляемая раса. Они получают значительный 
    бонус на дорогах (+50%) и в населенных пунктах, но медленнее передвигаются 
    по диким территориям - особенно по лесам, пустыням и снежным регионам.
    """
    
    # Модификаторы стоимости прохода для разных типов местности
    terrain_modifiers = {
        HexTerrainType.ROAD: 0.5,     # Люди отлично передвигаются по дорогам
        HexTerrainType.GRASS: 1.0,    # По равнинам - обычная скорость
        HexTerrainType.FOREST: 1.5,   # В лесах замедляются
        HexTerrainType.HILLS: 1.2,    # На холмах небольшое замедление
        HexTerrainType.MOUNTAIN: float('inf'),  # Горы непроходимы
        HexTerrainType.WATER: float('inf'),     # Вода непроходима
        HexTerrainType.SWAMP: 1.5,    # В болотах замедляются
        HexTerrainType.DESERT: 1.3,   # В пустыне замедляются
        HexTerrainType.SNOW: 1.4,     # В снегу замедляются
        HexTerrainType.LAVA: float('inf'),      # Лава непроходима
        HexTerrainType.CASTLE: 0.7,   # В замках быстрее (люди хорошо знают свои строения)
        HexTerrainType.VILLAGE: 0.6,  # В деревнях еще быстрее (родная среда)
        HexTerrainType.CAVE: 1.3,     # В пещерах замедляются
        HexTerrainType.WALL: float('inf'),  # Стены непроходимы
        HexTerrainType.START: 1.0,    # Стартовая позиция - обычная скорость
        HexTerrainType.END: 1.0       # Конечная позиция - обычная скорость
    } 