"""
@file orc.py
@brief Класс расы Орк.

@details
Реализация расы Орк с особенностями передвижения по различным типам местности.
Орки получают бонус в пустынях и холмах, но замедляются в воде и снегу.
"""

from .race import Race
from src.hex.hex_terrain_type import HexTerrainType


class Orc(Race):
    """
    @brief Класс представляющий расу Орк.
    
    @details
    Орки - сильная воинственная раса, хорошо приспособленная к суровым условиям.
    Они получают бонус при передвижении по пустыням, холмам и определенным типам 
    суровой местности, но менее комфортно чувствуют себя в воде и холодных регионах.
    """
    
    # Имя расы
    name = "Орк"
    
    # Описание расовых особенностей
    description = """
    Орки - выносливая воинственная раса. Они получают бонус в пустынях (-30% к
    стоимости передвижения) и на холмах, способны проходить через лаву с 
    небольшим штрафом, но медленнее передвигаются в воде и снежных регионах.
    """
    
    # Модификаторы стоимости прохода для разных типов местности
    terrain_modifiers = {
        HexTerrainType.ROAD: 0.9,     # По дорогам орки двигаются немного быстрее обычного
        HexTerrainType.GRASS: 0.9,    # По равнинам немного быстрее
        HexTerrainType.FOREST: 1.0,   # В лесах - обычная скорость
        HexTerrainType.HILLS: 0.7,    # На холмах значительный бонус
        HexTerrainType.MOUNTAIN: 1.1,  # В горах небольшое замедление
        HexTerrainType.WATER: 2.0,    # В воде значительно замедляются (но могут переходить)
        HexTerrainType.SWAMP: 1.1,    # В болотах небольшое замедление
        HexTerrainType.DESERT: 0.7,   # В пустыне получают бонус
        HexTerrainType.SNOW: 1.4,     # В снегу значительно замедляются
        HexTerrainType.LAVA: 2.0,     # По лаве могут проходить с большим штрафом
        HexTerrainType.CASTLE: 1.0,   # В замках - обычная скорость
        HexTerrainType.VILLAGE: 1.0,  # В деревнях - обычная скорость
        HexTerrainType.CAVE: 0.8,     # В пещерах небольшой бонус
        HexTerrainType.WALL: float('inf'),  # Стены непроходимы
        HexTerrainType.START: 1.0,    # Стартовая позиция - обычная скорость
        HexTerrainType.END: 1.0       # Конечная позиция - обычная скорость
    } 